bool LBP_IsDeclineResponse(uint16_t u16MessageLength, uint8_t *pMessageBuffer)
{
	return ((u16MessageLength > 1) && ((pMessageBuffer[0] & (LBP_DECLINE << 4)) == LBP_DECLINE));
}

/**********************************************************************************************************************/

/** LBP_EncodeDecline
 *
 ***********************************************************************************************************************
 *
 **********************************************************************************************************************/
uint16_t LBP_EncodeDecline(uint8_t *p_ext_addr, uint8_t *pMessageBuffer, uint16_t u16FreeBufferLength,
		uint8_t u8EAPIdentifier, uint8_t u8MediaType, uint8_t u8DisableBackupMedium)
{
	uint16_t u16EncodedLength = 0;

	if (u16FreeBufferLength >= ADP_ADDRESS_64BITS + 2) {
		pMessageBuffer[0] = (LBP_DECLINE  << 4) | (u8MediaType << 3) | (u8DisableBackupMedium << 2);
		pMessageBuffer[1] = 0; /* transaction id is reserved */

		memcpy(&pMessageBuffer[2], p_ext_addr, ADP_ADDRESS_64BITS);

		u16EncodedLength = ADP_ADDRESS_64BITS + 2;
		
		/* encode EAP Failure */
		u16EncodedLength += EAP_PSK_Encode_EAP_Failure(
				u8EAPIdentifier,
				u16FreeBufferLength - u16EncodedLength,
				(pMessageBuffer + u16EncodedLength)
				);
	}

	return u16EncodedLength;
}

/**********************************************************************************************************************/

/** LBP_Decode_Message
 ***********************************************************************************************************************
 *
 **********************************************************************************************************************/
bool LBP_Decode_Message(uint16_t u16MessageLength, uint8_t *pMessageBuffer, uint8_t *pu8MessageType,
		ADP_EXTENDED_ADDRESS *pEUI64Address, uint16_t *pu16BootStrappingDataLength, uint8_t **pBootStrappingData)
{
	bool bRet = false;

	if (u16MessageLength >= 2 + sizeof(ADP_EXTENDED_ADDRESS)) {
		*pu8MessageType = ((pMessageBuffer[0] & 0xF0) >> 4);

		memcpy(&pEUI64Address->m_au8Value, &pMessageBuffer[2], sizeof(ADP_EXTENDED_ADDRESS));

		*pBootStrappingData = &pMessageBuffer[2 + sizeof(ADP_EXTENDED_ADDRESS)];
		*pu16BootStrappingDataLength = u16MessageLength - (2 + sizeof(ADP_EXTENDED_ADDRESS));

		bRet = true;
	}

	return bRet;
}
